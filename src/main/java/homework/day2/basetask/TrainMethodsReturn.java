package homework.day2.basetask;

public class TrainMethodsReturn {
    int returnNewInt(int number) {
        return number * 3;
    }

    long returnNewLong(long number) {
        return number - 4;
    }

    char returnNewChar(char number) {
        return number;
    }

    float returnNewFloat(float number) {
        return number / 2;
    }

    double returnNewDouble(double number) {
        return number + 8;
    }

    short returnNewShort(short number) {
        return (short) (number - 1);
    }

    byte returnNewByte(byte number) {
        return (byte) (number * 2);
    }

    boolean returnNewBoolean(boolean number) {
        return false;
    }
}